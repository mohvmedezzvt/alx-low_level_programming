#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - Adding two integers.
 * @a: First integer.
 * @b: Second integer.
 * Return: The result.
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracting two integers.
 * @a: First integer.
 * @b: Second integer.
 * Return: The result.
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplying two integers.
 * @a: First integer.
 * @b: Second integer.
 * Return: The result.
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Dividing two integers.
 * @a: First integer.
 * @b: Second integer.
 * Return: The result.
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Moduling two integers.
 * @a: First integer.
 * @b: Second integer.
 * Return: The result.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
